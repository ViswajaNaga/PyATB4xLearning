# Ternary Operator --> Writing if else condition in single line

# If condition then do this
# else do that

# If my age is > 18 then I can vote
# else I can't vote

my_age = 18
print("I can vote" if my_age >= 18 else "I can't vote")

# user Input

age = int(input("enter age"))
print("I can vote" if age >= 18 else "I can't vote")

# If- Else condition

if age>17:
    print("I can vote")
else:
    print("I can't vote")