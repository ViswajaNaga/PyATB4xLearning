# LOOPS
# Repeat a block of code multiple times

x = list(range(1, 10, 1))
print(x)
for i in range(1, 10):  # range  is the inbuilt class
    print(i)
for viswaja in range(1, 5, 2):
    print(viswaja, end=" - ")  # by default end will be \n
for i in range(1,6):
    print("hello", i)

for number in range(10,0,-2):  #10,8,6,4,2,
    print(number)

my_list= list(range(10,0,-2))
print(my_list)